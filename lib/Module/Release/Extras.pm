=head1 NAME

Module::Release::Extras - additional functionality for Module::Release

=head1 SYNOPSIS

Don't C<use> this module, it exists to provide documentation.

=head1 DESCRIPTION

Module::Release::Extras contains subclasses of Module::Release that provide additional functionality during the release process.  The available modules are:

=over 4

=item *

Module::Release::ModuleBuild

=item *

Module::Release::Subversion

=back

See the documentation for those modules for additional information.

